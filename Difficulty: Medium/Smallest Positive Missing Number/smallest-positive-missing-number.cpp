class Solution {
  public:
    // Function to find the smallest positive number missing from the array.
    int missingNumber(vector<int> &arr) {
        // Your code here
        set<int> s;
        for(int a:arr){
            if(a>=0){
                s.insert(a);
            }
        }
        for(int i=1;i<10^5;i++){
            if(s.find(i)==s.end()){
                return i;
            }
        }
    }
};

