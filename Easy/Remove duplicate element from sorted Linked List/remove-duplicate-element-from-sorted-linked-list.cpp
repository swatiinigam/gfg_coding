//Function to remove duplicates from sorted linked list.
Node *removeDuplicates(Node *head)
{
 // your code goes here
 Node*curr=head;
 Node*temp=curr->next;
 while(temp!=nullptr){
     if(curr->data==temp->data){
         if(temp->next!=nullptr){
         temp=temp->next;
         curr->next=temp;
         }
         else{
         curr->next=nullptr;
         break;
         }
     }
     else{
         temp=temp->next;
         curr=curr->next;
     }
 }
 return head;
}